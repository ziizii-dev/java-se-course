	
	public class math_class {
	
		public static void main(String[] args) {
	           
			int result = Math.max(90,100);
			System.out.println("Max:"+result);
			float result1 = Math.max(9.0f,10.0f);
			System.out.println("Float:"+result1);
			double result2 = Math.max(999.0d,100.0d);
			System.out.println("Double:"+result2);
			 int result3 = (int) Math.ceil(999.8);
			System.out.println("Ceil:"+result3);
			
			 int result4 = (int) Math.abs(999.8);
				System.out.println("Abs:"+result4); //it means from 0 - is 999 each difference;
		   int result5 = (int) Math.pow(3,3);
			System.out.println("Power:"+result5);
					
			 int result6 = (int) Math.sqrt(25);
				System.out.println("Square:"+result6);
				
		}
	
	}
